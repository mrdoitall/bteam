    static function getUrlForName($name)
    {
        $name = preg_replace('/\s+/', ' ', $name);
        return preg_replace('/\-+/', '-', preg_replace("/[^A-Za-z0-9-]/", '', strtolower(str_replace(' ', '-', static::replaceUnicodeVN($name)))));
    }

    static function displayDateTime($unix)
    {
        $date = new \DateTime();
        $date->setTimestamp($unix);
        return $date->format('H:i:s d-m-Y');
    }

    static function displayDate($unix)
    {
        $date = new \DateTime();
        $date->setTimestamp($unix);
        return $date->format('d-m-Y');
